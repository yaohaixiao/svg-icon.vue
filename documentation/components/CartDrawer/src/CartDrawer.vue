<template>
  <base-drawer
    ref="drawer"
    :title="title"
    header-border
    size="medium"
    :buttons="buttons"
    custom-class="card-drawer"
    @close="onClose">
    <template v-slot:tabs>
      <base-tab-nav
        v-model="active"
        stretch
        :tabs="tabs"
        class="cart-drawer__tabs" />
    </template>
    <div class="cart-drawer__main">
      <ul
        v-if="active === 'icon'"
        class="cart-drawer__list">
        <template v-if="items.length > 0">
          <cart-drawer-item
            v-for="(item, i) in items"
            :key="`item-${i}`"
            :symbol="item"
            :index="i"
            @delete="onDelete" />
        </template>
        <cart-drawer-item
          v-else
          is-empty>
          <base-empty />
        </cart-drawer-item>
      </ul>
      <textarea
        v-else
        readonly
        :value="code"
        class="cart-drawer__code" />
    </div>
  </base-drawer>
</template>

<script>
/**
 * CartDrawer.vue - CartDrawer 组件
 * =============================================================
 * Created By: Yaohaixiao
 * Update: 2022.11.10
 */
import BaseDrawer from 'components/BaseDrawer'
import BaseTabNav from 'components/BaseTabNav'
import BaseEmpty from 'components/BaseEmpty'

import CartDrawerItem from '../../CartDrawerItem'

import { clearStorage } from '$utils/storage'
import { copyToClipboard, createAndDownloadFile } from '$utils/utils'

export default {
  name: 'CartDrawer',
  componentName: 'CartDrawer',
  components: {
    BaseDrawer,
    BaseTabNav,
    BaseEmpty,
    CartDrawerItem
  },
  props: {
    title: {
      type: String,
      default: '购物车'
    },
    items: {
      type: Array,
      default: () => []
    }
  },
  data() {
    return {
      active: 'icon',
      tabs: [
        {
          label: 'SVG 图标',
          value: 'icon'
        },
        {
          label: 'JavaScript 代码',
          value: 'code'
        }
      ],
      buttons: []
    }
  },
  computed: {
    isIconView() {
      return this.active === 'icon'
    },
    disabled() {
      return this.items.length === 0
    },
    code() {
      const iconSet = {
        title: 'SvgIcon 图标集',
        code: 'SvgIconSet',
        symbols: this.items
      }
      const json = JSON.stringify(iconSet, null, 2)

      return `// Generated by svg-icon.vue\nconst SvgIconSet = ${json}\n\nexport default SvgIconSet\n`
    },
    svg() {
      return (
        '<!-- Generated by svg-icon.vue -->\n' +
        '<svg id="svg-icons" aria-hidden="true" style="position:absolute;width:0;height:0;overflow:hidden;">\n' +
        this.items.join('\n') +
        '\n</svg>'
      )
    }
  },
  watch: {
    items() {
      this.update()
    }
  },
  created() {
    this.update()
  },
  mounted() {
    this.$subscribe('show:drawer', this.show)
    this.$subscribe('hide:drawer', this.hide)
  },
  beforeDestroy() {
    this.$unsubscribe('show:drawer', this.show)
    this.$unsubscribe('hide:drawer', this.hide)
  },
  methods: {
    update() {
      this.buttons = [
        {
          name: 'cancel',
          text: '取消',
          size: 'small'
        },
        {
          name: 'clean',
          text: '清空',
          icon: 'trash',
          disabled: this.disabled,
          action: () => {
            clearStorage('svg.icon.set')

            this.$message.success({
              round: true,
              message: `购物车已清空！`
            })

            this.$broadcast('clean:cart')
          }
        },
        {
          name: 'download',
          text: '下载',
          icon: 'download',
          disabled: this.disabled,
          action: () => {
            if (this.isIconView) {
              createAndDownloadFile('svg-icon-set.svg', this.svg)
            } else {
              createAndDownloadFile('svg-icon-set.js', this.code)
            }
          }
        },
        {
          name: 'confirm',
          text: '复制',
          icon: 'copy',
          size: 'small',
          type: 'primary',
          disabled: this.disabled,
          action: () => {
            copyToClipboard(this.isIconView ? this.svg : this.code)
            this.$message.success({
              round: true,
              message: `代码已复制！`
            })
          }
        }
      ]
    },
    show() {
      this.$refs.drawer.open()
    },
    hide() {
      this.$refs.drawer.close()
    },
    close() {
      setTimeout(() => {
        this.$broadcast('show:cart')
      }, 300)
    },
    onClose() {
      this.close()
    },
    onDelete({ symbol, name }) {
      this.$broadcast('remove:icon', symbol)

      this.$message.success({
        round: true,
        message: `图标 ${name} 已移除购物车！`
      })
    }
  }
}
</script>

<style scoped lang="less">
@import './cart-drawer';
</style>
